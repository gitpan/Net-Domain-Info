package Net::Domain::Info::Whois;

use Class::Easy;

use Net::Whois::Raw ();
use Net::Domain::ExpireDate ();

sub _init {
	my $class  = shift;
	my $parent = shift;
	
	make_accessor ($parent, 'fetch_whois', default => \&fetch_whois);
	make_accessor ($parent, 'raw_whois');
	make_accessor ($parent, 'created');
	make_accessor ($parent, 'expires');
	make_accessor ($parent, 'contacts');
	make_accessor ($parent, 'whois_failed', is => 'rw');
	make_accessor ($parent, 'whois_defined', is => 'rw');
}

sub fetch_whois {
	my $self = shift;
	
	my $domain = $self->name;
	
	$Net::Whois::Raw::CHECK_FAIL = 2;
	$Net::Whois::Raw::CHECK_EXCEED = 1;
	
	my $whois;
	eval {
		$whois = $self->{raw_whois} = Net::Whois::Raw::whois (
			$domain, undef, $domain =~ /\.(?:com|net|org)$/i ? 'QRY_FIRST' : undef
		);
	};
	
	if ($@) {
		$self->whois_failed (1); # who knows? rate limit? connection failed?
		return;
	}
	
	$self->whois_failed (0);
	
	if (! defined $whois) {
		$self->whois_defined (0);
		return;
	}
	
	$self->whois_defined (1);
	
	my $tld = ($domain =~ /.*\.(\w+)/)[0];
	
	my ($creation, $expiration)
		= Net::Domain::ExpireDate::domdates_int ($whois, $tld);
	
	$self->{created} = $creation->ymd
		if defined $creation;
	$self->{expires} = $expiration->ymd
		if defined $expiration;
	
	# debug "$domain: created: $creation expires: $expiration";
	
	$self->{contacts} = [];
	
	my $emails = find_emails ($whois);
	
	if (!scalar keys %$emails and $domain =~ /\.(?:com|net|org)$/i) {
		eval {
			$whois = Net::Whois::Raw::whois (
				$domain, undef, 'QRY_LAST'
			);
			
			if (defined $whois and $whois ne $self->{raw_whois}) {
				$self->{raw_whois} .= $whois;
				$emails = find_emails ($whois);
			}
		};
	}
	
	foreach (keys %$emails) {
		push @{$self->{contacts}}, {proto => 'email', address => $_};
	}
}

