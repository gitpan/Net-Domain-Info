sub find_emails {
	my $text = shift;

	my %emails;
	
	while ($text =~ /($RFC822PAT)/gso) {
		my $email = $1;
		$email =~ s/\s+//g;
		$emails{$email} = 1;
	}
	
	return \%emails;
}

