=head1 NAME

Net::Domain::Info::Whois - Net::Domain::Info plugin for requesting whois information

=head1 SYNOPSIS

This module provides Whois data fetch and simple parsing (currently only
create/expire dates and email information)

	use Net::Domain::Info qw(::Whois); # used Whois plugin

	Net::Domain::Info->new ($domain);

	$domain_info->fetch_whois;

	$domain_info->created;
	$domain_info->expires;

	$domain_info->contacts;

=head1 METHODS

=head2 fetch_whois

Fetches Whois and stores Whois information in the object. Then parses
"create" and "expire" dates for domain and tries to fetch email addresses.
When no address is found, tries to receive the last Whois in the chain
and searches for addresses again.

=cut

=head2 created

Accessor for domain record creation date.

=cut

=head2 expires

Accessor for domain record expiration date.

=cut

=head2 contacts

Returns arrayref of contacts with prototypes. Currently
only "eâ€“mail" prototype ia supported. In the future support
for phone and people contacts is planned.

=cut

=head2 whois_failed

Returns status of Whois fetch. As example, you can receive undefined
Whois data. This can happen because server failed, or you exceeded rate
limit, or domain does not exist. whois_failed flag is set only when
something is wrong with Whois server and you can (and must!) recheck
Whois later.

=cut

